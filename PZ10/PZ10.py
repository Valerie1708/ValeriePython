#{В магазинах имеются следующие товары. Магнит – молоко, соль, сахар. Пятерочка –
#мясо, молоко, сыр. Перекресток – молоко, творог, сыр, сахар. Определить:
#1. полный список всех товаров.
#2. в каких магазинах можно приобрести одновременно молоко и сыр.
#3. в каких магазинах можно приобрести сахар
# Данные о магазинах и товарах
Магнит = {"молоко", "соль", "сахар"}
Пятерочка = {"мясо", "молоко", "сыр"}
Перекресток = {"молоко", "творог", "сыр", "сахар"}

# 1. Полный список всех товаров
все_товары = Магнит.union(Пятерочка).union(Перекресток)
print("Полный список товаров:", все_товары)

# 2. Магазины с молоком и сыром
магазины_с_молоком_и_сыром = []
if {"молоко", "сыр"}.issubset(Магнит):
    магазины_с_молоком_и_сыром.append("Магнит")
if {"молоко", "сыр"}.issubset(Пятерочка):
    магазины_с_молоком_и_сыром.append("Пятерочка")
if {"молоко", "сыр"}.issubset(Перекресток):
    магазины_с_молоком_и_сыром.append("Перекресток")

print("Магазины с молоком и сыром:", магазины_с_молоком_и_сыром)

# 3. Магазины с сахаром
магазины_с_сахаром = []
if "сахар" in Магнит:
    магазины_с_сахаром.append("Магнит")
if "сахар" in Пятерочка:
    магазины_с_сахаром.append("Пятерочка")
if "сахар" in Перекресток:
    магазины_с_сахаром.append("Перекресток")

print("Магазины с сахаром:", магазины_с_сахаром)
